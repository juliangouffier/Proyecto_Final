    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(pesoFinal == null || pesoFinal.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Debe ingresar un valor para el peso final.", "Advertencia", JOptionPane.WARNING_MESSAGE);
        } else {
            dieta.setFechaFin(new Date());
            dieta.setPesoFinal(Double.valueOf(pesoFinal.getText()));
            dietaData.modificarDieta(dieta);
            JOptionPane.showMessageDialog(null, "Dieta Finalizada con exito.", "Completado", JOptionPane.INFORMATION_MESSAGE);
            this.setVisible(false);
            this.verDieta.setVisible(false);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void pesoFinalKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_pesoFinalKeyTyped
        char c = evt.getKeyChar();
        String text = pesoFinal.getText();
        if (!Character.isDigit(c) && c != '.') {
            evt.consume(); 
        } else if (c == '.' && text.contains(".")) {
            evt.consume();
        }
    }//GEN-LAST:event_pesoFinalKeyTyped

